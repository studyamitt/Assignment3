### Task 2: Using the Math Module for Calculations
import math

val1=int(input("Enter the number: "))
#1. Square root of the number
sroot=math.sqrt(val1)
print("Square Root is:",sroot)

#2. Natural logarithm (log base e) of the number
lth1=math.log(val1)
print("Natural logarithm is",lth1)

#3. Sine of the number (in radians)
sn=math.sin(val1)
print("Sine of the number is:",sn)